
public class ItemVersion {
    
   
    int WTS;
    int RTS;

  public  ItemVersion(int wts, int rts){
  
      WTS = wts;
      RTS = rts;
  } 
  
  public int getWTS(){
      return WTS;
  }
  
  public void setWTS(int wts){
      WTS = wts;
  }
  
  public int getRTS(){
      return RTS;
  }
  
  public void setRTS(int rts){
      RTS = rts;
  }
  

  
 
}
